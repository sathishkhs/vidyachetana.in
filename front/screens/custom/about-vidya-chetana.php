                    <Currently, class="about-page__center-text">Vidya Chetana is a Scholarship Programme initiated by Youth for Seva in 2009. This
programme was conceived with the objective to support education of students hailing from
underprivileged socio-economic backgrounds. The programme aims at providing scholarship
to students pursuing PUC (10+2), Degree, Engineering, and Medical studies. Our
beneficiaries include poor students, handicapped students, and students who lost their
parents during COVID-19 pandemic. Currently, we are supporting 5,775 students across 10
states. </p>
                    <p class="about-page__center-text">Education is one of the most potential and sustainable way to put an end to illiteracy and
poverty. Education not only empowers the student, but also makes a positive shift for the
whole family. Through Vidya Chetana, we want to empower students with education, so that
they can achieve their aspirations and grow up to be self-dependent and resourceful
citizens.</p>
<p class="thm-text">The total funds required to support each student in different streams are as follows:</p>
<ul>
    <li>To support 1 student in PUC (10+2), we require INR 15,000</li>
    <li>To support 1 student in Degree, we require INR 25,000</li>
    <li>To support 1 student in Engineering, we require INR 50,000</li>
    <li>To support 1 student in Medical, we require INR 50,000</li>
</ul>
