                <p>
                We have come across many inspiring stories of women during our journey and it only makes us want to do more for them. We have kick-started several projects and have many plans in the pipeline to empower women and help them in every way.
                <br><br>
                Under our program 'Savitrichya Leki Chalalya Pudhe', we distribute cycles to school and college girl students who need it the most, especially in the rural areas. Lack of proper transportation facilities in rural India hinders these girl students to continue their education. These cycles help them to travel to schools and colleges, even if it is far away from their home. Maharashtra had a great woman leader, Savitribai Phule, a pioneer, thinker and a reformer who spearheaded the girl-child education and this project is our humble tribute to her.
                <br><br>
                We have conducted various skill development courses for women who are associated with self-help groups formed under the huge Raigad Zilla Mahila Bachatgat Sakshamikaran Foundation. The skills training workshops included tailoring, beauty treatments, jewellery-making, cooking and candle-making.
                <br><br>
