                    We have supported medical expenses to the number of underprivileged people who are under chemotherapy, for breast cancer surgery, and leg amputation surgery.
                    <br><br>
                    <b>Covid Care</b>
                    <br>
                    We set-up and operated a full-fledged 100-bed care centre in Alibaug in just two weeks. The centre is equipped with: 
                    <br>
                    <ul style="color: #333333;">
                        <li>400+ beds</li>
                        <li>23 oxygen concentrator machines</li>
                        <li>27 oxygen cylinders</li>
                        <li>Free medicines and consultancy</li>
                        <li>PPE kits and N95 masks for staff and patients</li>
                        <li>Free meals (breakfast, lunch, evening snacks, and dinner) for patients and staff</li>
                        <li>Distributed 500 free meals per day to patients in other hospitals and in other lockdown-affected areas</li>
                        <li>Successfully treated 600 patients with no deaths reported</li>
                        <li>Free Covid-19 vaccination drive benefiting over 8,000 people and still counting</li>
                        <li>Donated 15 ICU-bed units to the District Civil Hospital, Raigad, of which 5 are pediatric ICU beds with advance equipment, portable X-ray machine</li>
                        <li>Donated ambulance to tribal villages in Raigad district </li>
                        <li>Conducted free eye check-up and cataract operation camp</li>
                        <li>Donated 105 beds to various Covid-19 care centres in Maharashtra</li>
                        <li>Donated 16 advanced oxygen concentrator machines</li>
                        <li>Donated 33 oxygen cylinders</li>
                        <li>Donated 100 oximeters</li>
                        <li>Donated 75 infrared temperature guns</li>
                    </ul>

