                <p>
                    India’s literacy rate is 77.7%, of which, urban India accounts for 87.7% rate and rural India is only at 73.5%. Several factors are at play for rural India children missing out on holistic education. Some of them being lack of resources, poor availability of schools, financial condition, poor transportation, etc. Education is our primary service area and it is our aim to make education available even in the remotest areas. In our effort to ensure that each child is able to learn, grow and live a good life, we have so far:
                    <br>
                    <ul style="color: #333333;">
                        <li>Awarded scholarship to 580 students from various grades i.e. from kindergarten to post graduation</li>
                        <li>Constructed 93 classrooms for various rural schools</li>
                        <li>Donated 23 computers to various rural schools</li>
                        <li>Donated academic kits to 1,250 underprivileged rural students</li>
                        <li>Donated 700 tabs to underprivileged students to continue education during the Covid 19 pandemic. This is an ongoing project. Visit us at <a href="http://www.schoolontab.org" target="_blank">www.schoolontab.org</a></li>
                        <li>Donated over 4800 bicycles to underprivileged girl students so that they can continue their education. This will also help to reduce drop-out rates.</li>
                        <li>Children from the tribal community face tougher situations to even get primary education. We put together an academic kit consisting of a school bag, textbooks, note books, compass box, pen, school uniforms, shoes, umbrella, tiffin box, water bottle and writing pads and distributed it to 60 tribal children.</li>
                        <li>We also provided 125 benches to various schools to ensure students have the perfect setting of a school. We are in the process of identifying more schools that need benches.</li>
                    </ul>
