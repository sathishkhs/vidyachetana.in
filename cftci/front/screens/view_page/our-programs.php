                <p style="text-align: justify">
                    Savitribai Phule was an Indian social reformer, educationalist, and a poetess from Maharashtra. She is regarded as the first female teacher of India. Our program to distribute bicycles to girl students is a tribute to the great reformer.
                    <br><br>
                    Today, especially in rural areas, the drop-out rate of girl students from school is increasing. Girl students face several problems such as unsupportive lifestyle, hostile geographical location of schools and colleges, social stigma, and gender inequality.
                    <br><br>
                    When we did our mapping and research, one of the girls mentioned to us, and we quote, “We are more frightened of rapes and human attacks than attacks by ferocious animals.”
                    <br><br>
                    This, unfortunately, is the harsh reality of India, especially in rural areas where homes are far and girls have to cover long distances to reach anywhere. It makes them vulnerable to sexual assaults.
                    <ul style="color: #333333;">
                        <li>
                            Bicycles help them to commute easily without wasting time on walking, or waiting for public transport.
                        </li>
                        <li>
                            CFTI is planning to distribute bicycles across Maharashtra by giving girls bicycles. We aim to help them in continuing schooling and even complete college education. This will also serve as a motivation for the girls and help them to commute easily.
                        </li>
                        <li>
                        CFTI has the vision to distribute 100000 cycles to the girl students across India. So far, we have successfully distributed 4800 cycles in various parts of Maharashtra.
                        </li>
                        <li>
                            Our volunteers who are working at several grassroots areas carry out a detailed mapping of girl students in need of the bicycle and after getting accurate data, we distribute the bicycles to girls in dire need. 
                        </li>
                        <li>
