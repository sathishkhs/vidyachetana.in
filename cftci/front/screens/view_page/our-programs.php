                        </li>
                        <li>
                            We select the students to provide these tabs based on our detailed mapping and research.
                        </li>
                        <li>
                            Each needy student submits an application to our representatives, requesting a tab along with their school progress report card, parents’ income proof and Aadhar card.
                        </li>
                        <li>
                            Once we receive the applications, our representatives visit their homes and verify the records with school authorities. Post this careful examination, an applicant becomes eligible to receive a tab from CFTI. 
