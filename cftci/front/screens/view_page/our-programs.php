                <p style="text-align: justify">
                    Our distinctive program, known as the 'Small Grant Programme', is introduced mainly to disburse help to the people who are in urgent need but do not have access to the correct resources. Through this programme, we wish to create a ray of hope for the person who is in immediate need. For eg. if there has been an accident and an urgent surgery is needed or someone has to pay the fees to avoid cancellation of an admission in an educational institute. We verify applicants on priority and provide them with help within 24 hours. Unlike other programs, this provides quick and timely help to the needy person. 
                    <br><br>
                    We have also provided financial aid to a few local artists in order to fulfill their dream of creating a professional music video and traveling abroad to perform. 
                    <br><br>
                    We also support local sportsmen to organize small scale tournaments and competitions with the intention to appreciate local talent and promote sports in rural areas. We also provide legal assistance to those in need but cannot afford to hire legal help.

