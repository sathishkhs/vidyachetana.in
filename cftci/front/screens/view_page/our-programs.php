                    Gadgets open up a window to the world for these children. Apart from online classes, they have access to extensive information and many times an opportunity to showcase their talent to the world. CFTI has pledged that no student should miss the online classes and numerous opportunities only because of the non-availability of gadgets. And as our commitment in action, till now we have:
